
public class Schedule {
	int studentID;
	String semester;
	int year;
	int classID;
	String className;
	String times;
	
	public Schedule(int studentID, String semester, int year,
			int classID, String className, String times) {
		this.studentID = studentID;
		this.semester = semester;
		this.year = year;
		this.classID = classID;
		this.className  = className;
		this.times = times;
	}
	
}
